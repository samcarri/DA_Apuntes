bool resuelveCaso() {
    int F, C;
    char c;
    cin >> F >> C;
    if (!std::cin) return false;
    
    for (int i = 0; i < F; i++) {
        for (int j = 0; j < C; j++) {
            cin >> c;
        }
    }

    return true;
}

